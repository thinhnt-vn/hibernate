    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int row = table.getSelectedRow();
        BoPhanDAO bpDao = new BoPhanDAO();
        Bophan selectedBP = null;
        String text = tenBPTextfield.getText();
        if (row >= 0) {
            int id = Integer.valueOf(table.getValueAt(row, 0).toString());
            selectedBP = bpDao.find(id);
            if (!text.equals(selectedBP.getTenbp())) {
                selectedBP.setTenbp(text);
            }
        } else {
            selectedBP = new Bophan();
            selectedBP.setTenbp(text);
        }
        boolean sussess = bpDao.updateOrSave(selectedBP);
        if (sussess) {
            JOptionPane.showMessageDialog(null, "Cập nhật thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            loadData();
        } else {
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại", "Thoonh báo", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = table.getSelectedRow();
        BoPhanDAO bpDao = new BoPhanDAO();
        Bophan selectedBP = null;
        if (row >= 0) {
            int id = Integer.valueOf(table.getValueAt(row, 0).toString());
            selectedBP = bpDao.find(id);
            boolean success = bpDao.delete(selectedBP);
            if (success) {
                JOptionPane.showMessageDialog(null, "Xóa thành công", "Thành công", JOptionPane.INFORMATION_MESSAGE);
                loadData();
            }else{
                JOptionPane.showMessageDialog(null, "Xóa thất bại", "Thoonh báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

