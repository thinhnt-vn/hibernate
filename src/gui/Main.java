    public void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Mã bộ phận");
        model.addColumn("Tên bộ phận");
        BoPhanDAO boPhanDAO = new BoPhanDAO();
        List<Bophan> bps = boPhanDAO.findAll();
        for (Bophan bp : bps) {
            model.addRow(new String[]{bp.getMabp() + "", bp.getTenbp()});
        }
        table.setModel(model);
    }

